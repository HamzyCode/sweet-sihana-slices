// Function to get products by category
export const getProductsByCategory = (category) => {
  return category === 'all' 
    ? products 
    : products.filter(product => product.category === category);
};

// Function to get product by ID
export const getProductById = (id) => {
  const productId = parseInt(id);
  return products.find(product => product.id === productId);
};

// Function to get related products (same category, excluding current product)
export const getRelatedProducts = (productId, categoryFilter) => {
  const currentProduct = getProductById(productId);
  
  if (!currentProduct) return [];
  
  return products
    .filter(product => product.category === currentProduct.category && product.id !== currentProduct.id)
    .slice(0, 3);
};

// Get unique categories for filter buttons
export const getCategories = () => {
  const categories = new Set(products.map(product => product.category));
  return Array.from(categories);
};

// Get unique occasions
export const getOccasions = () => {
  const occasions = new Set();
  products.forEach(product => {
    if (product.occasions) {
      product.occasions.forEach(occasion => occasions.add(occasion));
    }
  });
  return Array.from(occasions);
};
